var area = document.getElementById("area")
function clicar(){
    area.innerText = 'Clicou!'
    area.style.background = 'green'

}
function sair(){
    area.innerText = 'Saiu!'
    area.style.background = 'red'

}
function entrar(){
    area.innerText = 'entrou!'
    area.style.background = 'green'
}
